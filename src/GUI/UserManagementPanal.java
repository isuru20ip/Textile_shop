    private void loardUser() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            ResultSet rs = DB1.search("SELECT * FROM `user` "
                    + "INNER JOIN `user_status` ON user_status.id = user.user_status_id "
                    + "INNER JOIN `user_type` ON user_type.id = user.id");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("username"));
                v.add(rs.getString("type"));
                v.add(rs.getString("status"));
                model.addRow(v);
            }
            jTable1.setModel(model);
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void loardType() {
        try {
            ResultSet re = DB1.search("SELECT * FROM `user_type`");

            Vector<String> v = new Vector<>();
            v.add("Select Type");
            while (re.next()) {
                v.add(re.getString("type"));
            }
            DefaultComboBoxModel boxModel = (DefaultComboBoxModel) jComboBox1.getModel();
            boxModel.removeAllElements();
            boxModel.addAll(v);
            jComboBox1.setSelectedIndex(0);
            
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

